var a = prompt ('Digite o valor de A:')
var b = prompt ('Digite o valor de B: ')

var numeroA = parseInt(a)
var numeroB = parseInt(b)

var numeroC = numeroA - numeroB
var numeroD = numeroA * numeroB
var numeroE = numeroA / numeroB
var numeroF = (numeroA + numeroB)*2

alert ('O resultado 1 é: ' + numeroC)
alert ('O resultado 2 é: ' + numeroD)
alert ('O resultado 3 é: ' + numeroE)
alert ('O resultado 4 é: ' + numeroF)