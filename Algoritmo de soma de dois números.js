var a = prompt ('Digite o número A:');
var b = prompt ('Digite o número B:');
  
  //Conversão de texto para número
  var numeroA = parseInt (a)
  var numeroB = parseInt (b)
  
  //Armazenamento da soma A + B 
  var c = a + b
  var numeroC = numeroA + numeroB
alert ('Resultado com texto: ' + c)
alert ('Resultado com números ' + numeroC )
